import turtle
t = turtle.Turtle()
turtle.bgcolor("white")
t.speed("fast")
t.pensize(12)
t.penup()
t.goto(-130,180)
t.pendown()
t.color("black")
t.write("Mercedes-Benz", font=("helvetica", 30))

def dark_gray():
 t.pencolor("dark grey")
 t.penup()
 t.goto(0, -150)
 t.pendown()
 t.circle(150)

 t.penup()
 t.goto(0, 150)
 t.pendown()
 t.fillcolor("dark grey")
 t.begin_fill()
 t.pensize(6)
 t.goto(-20,-15)
 t.goto(20,-15)
 t.goto(0,150)
 t.end_fill()

 t.pensize(12)
 t.penup()
 t.goto(-120,-80)
 t.pendown()
 t.fillcolor("dark grey")
 t.begin_fill()
 t.pensize(6)
 t.goto(-20,5)
 t.goto(20,-5)
 t.goto(-120,-80)
 t.end_fill()

 t.pensize(12)
 t.penup()
 t.goto(120,-80)
 t.pendown()
 t.fillcolor("dark grey")
 t.begin_fill()
 t.pensize(6)
 t.goto(-20,-5)
 t.goto(20,5)
 t.goto(120,-80)
 t.end_fill()

def gray():
 t.pencolor("grey")
 t.penup()
 t.goto(0, -150)
 t.pendown()
 t.circle(150)

 t.pensize(12)
 t.penup()
 t.goto(120,-80)
 t.pendown()
 t.fillcolor("grey")
 t.begin_fill()
 t.pensize(6)
 t.goto(-20,-5)
 t.goto(20,5)
 t.goto(120,-80)
 t.end_fill()

 t.pensize(12)
 t.penup()
 t.goto(-120,-80)
 t.pendown()
 t.fillcolor("grey")
 t.begin_fill()
 t.pensize(6)
 t.goto(-20,5)
 t.goto(20,-5)
 t.goto(-120,-80)
 t.end_fill()

 t.penup()
 t.goto(0, 150)
 t.pendown()
 t.fillcolor("grey")
 t.begin_fill()
 t.pensize(6)
 t.goto(-20,-15)
 t.goto(20,-15)
 t.goto(0,150)
 t.end_fill()






while(True):
    dark_gray()
    gray()

turtle.done()
