import turtle
t = turtle.Turtle()
turtle.bgcolor("white")
t.speed("slow")
t.pensize(40)
t.pencolor("dark grey")
t.penup()
t.goto(0, -150)
t.pendown()
t.circle(150)

t.pensize(10)
t.pencolor("grey")
t.penup()
t.goto(0, -130)
t.pendown()
t.circle(130)

t.pensize(5)
t.pencolor("grey")
t.penup()
t.goto(0, -170)
t.pendown()
t.circle(170)

t.pensize(6)
t.pencolor("blue")
t.penup()
t.goto(-130, 30)
t.pendown()
t.fillcolor("blue")
t.begin_fill()
t.goto(130, 30)
t.goto(130, -30)
t.goto(-130, -30)
t.goto(-130, 30)
t.end_fill()

t.penup()
t.goto(-125,-45)
t.pendown()
t.color("white")
t.write("VOLVO", font=("Cambria", 60, 'bold'))

t.pensize(3)
t.pencolor("grey")
t.penup()
t.goto(90,140)
t.pendown()
t.fillcolor("dark grey")
t.begin_fill()
t.goto(105, 155)
t.goto(75, 175)
t.goto(150, 190)
t.goto(150, 120)
t.goto(125, 140)
t.goto(112, 126)
t.end_fill()

t.hideturtle()
turtle.done()
